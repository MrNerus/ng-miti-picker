def flatten_calendar(calendar_days):
    """
    Flattens a 2D calendar array into a 1D list of cumulative day offsets.
    
    Args:
        calendar_days (list of list of int): The nested year-month structure.
        
    Returns:
        list of int: A flat list where each index represents 
                     (year_index * 12 + month_index).
    """
    flat_starts = []
    cumulative_days = 0
    
    print('[')
    
    for year_data in calendar_days:
        for month_length in year_data:
            flat_starts.append(cumulative_days)
            print(cumulative_days, end=", ")
            cumulative_days += month_length
        print(" # ")
        
    print(']')
    return flat_starts

